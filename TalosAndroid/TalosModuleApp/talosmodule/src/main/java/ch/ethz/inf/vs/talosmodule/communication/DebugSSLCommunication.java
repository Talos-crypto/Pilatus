/**
 * Created by lukas on 08.12.15.
 * SSL communication without Certificate check NOT SECURE!!!! (For debugging)
 */
public class DebugSSLCommunication extends CommunicationHelper {

    public DebugSSLCommunication(String protocol, String ip, int port) {
        super(protocol, ip, port);
    }

    @Override
    public HttpURLConnection getConnection(URL url) throws IOException {
        HttpsURLConnection urlConnection =
                (HttpsURLConnection)url.openConnection();

        TrustManager[] trustAllCerts = new TrustManager[] { new X509TrustManager() {
            @Override
            public void checkClientTrusted(java.security.cert.X509Certificate[] chain, String authType) throws java.security.cert.CertificateException {

            }

            @Override
            public void checkServerTrusted(java.security.cert.X509Certificate[] chain, String authType) throws java.security.cert.CertificateException {

            }

            @Override
            public java.security.cert.X509Certificate[] getAcceptedIssuers() {
                return new java.security.cert.X509Certificate[0];
            }
        } };

        try {
            SSLContext sc = SSLContext.getInstance("TLS");

            sc.init(null, trustAllCerts, new java.security.SecureRandom());
            urlConnection.setSSLSocketFactory(sc.getSocketFactory());
        } catch (KeyManagementException e) {
            e.printStackTrace();
            return null;
        } catch (NoSuchAlgorithmException e) {
            e.printStackTrace();
            return null;
        }
        return urlConnection;
    }
}
